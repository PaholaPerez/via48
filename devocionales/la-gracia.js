// ================= ESTADO =================
let currentDay = Number(localStorage.getItem("via48_day")) || 1;

const steps = document.querySelectorAll(".step");
const modal = document.getElementById("modal");
const modalContent = document.querySelector(".modal-content");
const progress = document.getElementById("progress-fill");

// ================= INIT =================
init();

function init() {
  animatePageEntry();
  updateUI(currentDay);

  steps.forEach(step => {
    step.addEventListener("click", () => {
      goToDay(Number(step.dataset.day));
    });
  });

  modalContent.addEventListener("scroll", updateReadingProgress);
}

// ================= ANIMACIÓN ENTRADA =================
function animatePageEntry() {
  document.body.style.opacity = 0;
  document.body.style.transform = "translateY(20px)";

  window.addEventListener("load", () => {
    document.body.style.transition = "all 0.6s ease";
    document.body.style.opacity = 1;
    document.body.style.transform = "translateY(0)";
  });
}

// ================= NAVEGACIÓN =================
function goToDay(day) {
  currentDay = day;
  localStorage.setItem("via48_day", day);

  updateUI(day);
  openModal(day);
}

// ================= UI =================
function updateUI(day) {
  steps.forEach(step => {
    const d = Number(step.dataset.day);

    step.classList.remove("active", "completed");

    if (d === day) step.classList.add("active");
    if (d < day) step.classList.add("completed");
  });

  progress.style.width = (day / 5) * 100 + "%";
}

// ================= MODAL =================
function openModal(day) {
  const data = devocionales[day];

  document.body.style.overflow = "hidden";

  // HEADER
  document.getElementById("modal-title").innerHTML = `
    <h2>${data.titulo}</h2>
    <p style="color:#8b5cf6; margin-bottom:15px;">${data.versiculo}</p>
  `;

  // BODY
  const body = document.getElementById("modal-body");
  body.innerHTML = "";

  data.secciones.forEach(sec => {
    body.innerHTML += `
      <div class="bloque fade-in">
        <h4>${sec.titulo}</h4>
        <p>${sec.contenido}</p>
      </div>
    `;
  });

  modal.classList.add("show");

  setTimeout(() => restoreProgress(day), 100);
}

function closeModal() {
  modal.classList.remove("show");
  document.body.style.overflow = "auto";
  modalContent.scrollTop = 0;
}

// ================= PROGRESO LECTURA =================
function updateReadingProgress() {
  const scrollTop = modalContent.scrollTop;
  const scrollHeight = modalContent.scrollHeight - modalContent.clientHeight;
  const percent = (scrollTop / scrollHeight) * 100;

  localStorage.setItem(`reading_${currentDay}`, percent);
}

function restoreProgress(day) {
  const saved = localStorage.getItem(`reading_${day}`);

  if (saved) {
    const scrollHeight = modalContent.scrollHeight - modalContent.clientHeight;
    modalContent.scrollTop = (saved / 100) * scrollHeight;
  }
}

// ================= NAV =================
function goBack() {
  window.location.href = "devocionales.html";
}